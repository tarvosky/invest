trait FormatTrait {





    public  function numberToSSN($num) {
        $first  = substr($num,0,3);
        $sec = substr($num,3,2);
        $third  = substr($num,5,4);
        $formatted= $first ."-".$sec."-". $third;
        return $formatted;
    } 
 



    public  function numberToEIN($num) {
        $first  = substr($num,0,2);
        $sec = substr($num,2,7);
        $formatted= $first ."-".$sec;
        return $formatted;
    } 
 

    public  function ConvertToNumber($ssn) {
        $b = str_replace( '-', '',$ssn );
        return $b;
    } 



